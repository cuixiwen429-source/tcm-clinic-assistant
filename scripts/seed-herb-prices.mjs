// ─── Main ───
async function main() {
  // Load herbs from herbs.json
  const herbsPath = resolve(__dirname, "..", "prisma", "data", "herbs.json");
  const herbs = JSON.parse(readFileSync(herbsPath, "utf-8"));

  console.log(`Loaded ${herbs.length} herbs from herbs.json`);

  // Get existing HerbReference IDs from Turso
  const { rows: dbHerbs } = await db.execute("SELECT id, name FROM HerbReference");
  const herbMap = new Map(dbHerbs.map((r) => [r.name, r.id]));
  console.log(`Found ${dbHerbs.length} herbs in database`);

  let inserted = 0;
  let updated = 0;
  let skipped = 0;

  // Process in batches of 50
  const BATCH_SIZE = 50;
  const statements = [];

  for (const herb of herbs) {
    const herbId = herbMap.get(herb.name);
    if (!herbId) {
      skipped++;
      continue;
    }

    const price = specificPrices[herb.name] ?? categoryDefaults[herb.category] ?? 0.20;
    const origin = originDefaults[herb.category] || "中国";
    const spec = "统";

    // Use REPLACE (UPSERT) with our id format
    statements.push({
      sql: `INSERT OR REPLACE INTO HerbPrice (id, herbId, retailPrice, wholesalePrice, spec, origin, unit, sourceNote)
            VALUES (?, ?, ?, ?, ?, ?, 'g', '广东同仁堂零售参考价 2024-2025')
            ON CONFLICT(id) DO UPDATE SET retailPrice=excluded.retailPrice, wholesalePrice=excluded.wholesalePrice`,
      args: [`price-${herb.name}`, herbId, price, Math.round(price * 0.65 * 100) / 100, spec, origin],
    });
  }

  // Execute in batches
  for (let i = 0; i < statements.length; i += BATCH_SIZE) {
    const batch = statements.slice(i, i + BATCH_SIZE);
    for (const stmt of batch) {
      try {
        await db.execute({ sql: stmt.sql, args: stmt.args });
        inserted++;
      } catch (err) {
        console.error(`  Error on ${stmt.args[0]}:`, err.message);
      }
    }
    console.log(`  Batch ${Math.floor(i / BATCH_SIZE) + 1}/${Math.ceil(statements.length / BATCH_SIZE)} done (${inserted} so far)`);
  }

  // Verify
  const { rows: countRows } = await db.execute("SELECT COUNT(*) as cnt FROM HerbPrice");
  console.log(`\nDone! ${inserted} prices seeded. Total HerbPrice rows: ${countRows[0].cnt}`);
  if (skipped > 0) console.log(`Skipped ${skipped} herbs (not found in database)`);
}

main().catch(console.error);
